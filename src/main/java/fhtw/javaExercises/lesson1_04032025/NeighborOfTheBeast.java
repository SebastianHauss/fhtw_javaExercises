package fhtw.javaExercises.lesson1_04032025;

public class NeighborOfTheBeast {
    public static void main(String[] args) {
        int beast = 667;
        System.out.printf("the neighbor of the beast: %d!\n", beast);
        System.out.printf("the neighbor of the beast: %X!\n", beast);
        System.out.printf("the neighbor of the beast: %+10d!\n", beast);
        System.out.printf("the neighbor of the beast: %-10d!\n", beast);
        System.out.printf("the neighbor of the beast: %08d!\n", beast);
        System.out.printf("the neighbor of the beast: %+-12d!", beast);
    }
}
